class UserData {
  final String user_id;
  final String first_name;
  final String last_name;
  final int number;
  final String email;
  final String role;

  const UserData({
    required this.user_id,
    required this.first_name,
    required this.last_name,
    required this.number,
    required this.email,
    required this.role,
  });
}
